    private void initData(String origem, String destino){
        RotaBeanInterface rotaBean = null;
        InitialContext ctx = null;
        FuncionarioBeanInterface funcionarioBean = null;
        List<Rota> lista;
        List<Funcionario> listaFunc;
        try {
            ctx = new InitialContext();
            rotaBean = (RotaBeanInterface) ctx.lookup("ejb/RotaBean");
            funcionarioBean = (FuncionarioBeanInterface) ctx.lookup("ejb/FuncionarioBean");
            
            listaFunc = funcionarioBean.listarFuncionarios();
            
            for(int i=0; i<listaFunc.size();i++){
                comboFuncionario.addItem(listaFunc.get(i).getNomeFuncionario());
            }
            lista = rotaBean.listar();
            
            for(int i=0; i<lista.size();i++){
                if(lista.get(i).getDestinoRota().equals(destino) && lista.get(i).getOrigemRota().equals(origem)){
                    String poltronas[] = lista.get(i).getNumeroAssentosOcupados().split(",");
                    for(int j=1;j<=40;j++)
                        comboPoltronas.addItem(j);
                    for(int j=0;j<poltronas.length;j++)
                        comboPoltronas.removeItemAt(Integer.parseInt(poltronas[j])-1);
                    
                    preco.setText(String.valueOf(lista.get(i).getPreco()));
                    comboHorario.addItem(lista.get(i).getHorario());
                    comboData.addItem(lista.get(i).getDatas());
                }
            }
        } catch (NamingException ex) {
            Logger.getLogger(TelaVendaHorario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
