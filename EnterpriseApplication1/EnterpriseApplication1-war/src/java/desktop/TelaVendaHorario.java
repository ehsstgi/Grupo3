    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            
        ClienteBeanInterface clienteBean = null;
        RotaBeanInterface rotaBean = null;
        PassagemBeanInterface passagemBean = null;
        
        InitialContext ctx = null;
        
        try {
            ctx = new InitialContext();
            clienteBean = (ClienteBeanInterface) ctx.lookup("ejb/ClienteBean");
            rotaBean = (RotaBeanInterface) ctx.lookup("ejb/RotaBean");
            passagemBean = (PassagemBeanInterface) ctx.lookup("ejb/PassagemBean");
            String horario = (String)comboHorario.getSelectedItem();
            String data = (String)comboData.getSelectedItem();
            
            Rota rota = rotaBean.carregarRota(origem.getText(), destino.getText(), horario, data);
            
            rotaBean.escolherPoltrona(String.valueOf(comboPoltronas.getSelectedItem()), rota);
            
            if(clienteBean.efetuarCompraGuiche(rota, String.valueOf(comboPoltronas.getSelectedItem()), (String)comboFuncionario.getSelectedItem() )){
                JOptionPane.showMessageDialog(rootPane, "Compra Efetuada com sucesso.");
                TelaVendaHorario.this.dispose();
                
                Passagem p = passagemBean.listarPassagemGuiche(origem.getText(), destino.getText(), String.valueOf(comboPoltronas.getSelectedItem()), (String)comboData.getSelectedItem(), (String)comboHorario.getSelectedItem());
                ImprimirPassagem imprimir = new ImprimirPassagem(p);
                imprimir.setVisible(true);
            }
            
            
        } catch (NamingException ex) {
            Logger.getLogger(TelaVendaHorario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

