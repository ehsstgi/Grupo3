private void carregarRotasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_carregarRotasActionPerformed
        comboDataHorario.removeAllItems();
        comboHorario.removeAllItems();
    
        RotaBeanInterface rotaBean = null;
        InitialContext ctx = null;
        List<Rota> lista;
        
        String origem = (String) comboOrigemHorario.getSelectedItem();
        String destino = (String) comboDestinoHorario.getSelectedItem();
        
        try {
            ctx = new InitialContext();
            rotaBean = (RotaBeanInterface) ctx.lookup("ejb/RotaBean");
            lista = rotaBean.listar();

            for(int i=0; i<lista.size();i++){
                if(lista.get(i).getDestinoRota().equals(destino) && lista.get(i).getOrigemRota().equals(origem)){
                    comboHorario.addItem(lista.get(i).getHorario());
                    comboDataHorario.addItem(lista.get(i).getDatas());
                }
            }
        } catch (NamingException ex) {
            Logger.getLogger(TelaVendaHorario.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_carregarRotasActionPerformed

