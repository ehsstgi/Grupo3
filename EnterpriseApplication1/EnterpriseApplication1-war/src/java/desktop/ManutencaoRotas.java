    //Ações de gravação de Rota
    private void IncluirRotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IncluirRotaActionPerformed
        RotaBeanInterface rotaBean = null;      
        InitialContext ctx = null;
        
        try {
            ctx = new InitialContext();
            rotaBean = (RotaBeanInterface) ctx.lookup("ejb/RotaBean");
            String horarioRota = horario.getText();
            String dataRota = datas.getText();
            String origemRota = (String) comboOrigem.getSelectedItem();
            String destinoRota = (String) comboDestino.getSelectedItem();
            double precoRota = Double.parseDouble((String) comboPreco.getSelectedItem());
            
            Rota rota = new Rota();
            rota.setDatas(dataRota);
            rota.setDestinoRota(destinoRota);
            rota.setOrigemRota(origemRota);
            rota.setPreco(precoRota);
            rota.setHorario(horarioRota);

            if(rotaBean.cadastrarRota(rota))
                JOptionPane.showMessageDialog(rootPane, "Rota Incluida");
            else
                JOptionPane.showMessageDialog(rootPane, "Rota não pode ser incluida");
            
            } catch (NamingException ex) {
            Logger.getLogger(ManutencaoRotas.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
        
    }//GEN-LAST:event_IncluirRotaActionPerformed

